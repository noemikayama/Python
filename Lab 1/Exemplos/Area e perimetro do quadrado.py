# Program that given a square's side calculates area and perimeter

side = int(input("\n\n Insert square's side here: ")) # Get square's side
area = side ** 2 # Area = sideË†2
perimeter = side * 4 # Perimiter = side*4

print("\n\n With a square with a side ", side) # Prints the number equivalent to the side
print("\n\t The area is: ", area) # Prints the area with the given side
print("\n\t And the perimeter is: ", perimeter) # Prints perimeter with the given side
print("\n\n *END* \n\n")


