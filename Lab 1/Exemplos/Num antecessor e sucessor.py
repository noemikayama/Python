# Program that reads a number and prints the number before and after

num = int(input("\n\t Insert number here:")) # Get the integer number
bef = num-1 # Calculates the number before
aft = num+1 # Calculates the number after

print("\n\n The chosen number was: ", num) # Prints the chosen number
print("\n\t The number before ", num, "is: ", bef) # Prints the number before the given number
print("\n\t The number after ", num, "is: ", aft) # Prints the number after the given number
